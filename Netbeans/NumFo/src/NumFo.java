    /**
     * The getFactors method gets the factors of an inputted number.
     *
     * @param num the inputted number
     * @return a HashSet of the factors in order
     */
    static public Set<Integer> getFactors(int num) {
        Set<Integer> factors = new HashSet<>();
        int step = num % 2 == 0 ? 1 : 2;
        for (int i = 1; i <= Math.sqrt(num); i += step) {
            if (num % i == 0) {
                factors.add(i);
                factors.add(num / i);
            }
        }
        return factors;
    }

    /**
     * The isPrime method checks if a number is prime.
     *
     * @param num the inputted number
     * @return a boolean validating whether the number is prime:true or
     * not:false
     */
    static public Boolean isPrime(int num) {
        if (getFactors(num).size() > 2) {
            return (false);
        } else {
            return (true);
        }
    }

    /**
     * The getRood method calculates the square root of a number.
     *
     * @param num the inputted number
     * @return the square root, a double for accuracy
     */
    static public Double getRoot(int num) {
        Double root = Math.sqrt(num);
        return (root);
    }

