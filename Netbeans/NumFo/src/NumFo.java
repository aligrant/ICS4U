        String input = inputField.getText();

        //outputWiki.setPage(new URL("https://en.m.wikipedia.org/wiki/" + input + "#bodyContent"));
        int num = Integer.parseInt(input);

        String factors = Arrays.toString(getFactors(num).toArray());
        String prime;
        if (isPrime(num) == false) {
            prime = "not prime";
        } else {
            prime = "prime";
        }
        double root = getRoot(num);
