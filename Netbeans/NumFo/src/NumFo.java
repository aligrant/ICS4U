    /**
     * The showWiki method displays the web result for an inputted number. JFX
     * is used to make the site interactive.
     *
     * @param input the inputted string version of the number
     */
    public void showWiki(String input) {
        jframe.setVisible(true);
        JFXPanel fxpanel = new JFXPanel();
        jframe.add(fxpanel);
        Platform.runLater(() -> {
            WebEngine engine;
            WebView wv = new WebView();
            engine = wv.getEngine();

            fxpanel.setScene(new Scene(wv));
            engine.load("https://en.m.wikipedia.org/wiki/" + input);

        });

        jframe.setVisible(true);
    }

